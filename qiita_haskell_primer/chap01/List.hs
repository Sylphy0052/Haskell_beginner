-- Normal
-- {-
main = do
  print [1, 2, 3, 4, 5]
-- -}

-- Get Array element
{-
main = do
  print $ [1, 2, 3, 4, 5] !! 3
-}

-- Serial number(連番)
{-
main = do
  print [1..5]
-}

-- Link list(リスト結合)
{-
main = do
  print $ [1, 2, 3] ++ [4, 5]
-}

-- Insert List of first(先頭に挿入)
{-
main = do
  print $ 1:[2..5]
-}
{-
main = do
  print $ 1:2:[3..5]
-}
{-
main = do
  print $[1..4] ++ [5]
-}
